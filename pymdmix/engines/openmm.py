class OpenMMWriterError(Exception):
    """Error in OpenMM script generation."""

    pass


class OpenMMWriter:
    """
    Generate OpenMM Python simulation scripts for a Replica.

    Writes minimization, equilibration (5 NVT heating + 1 NPT steps),
    and per-step production scripts plus a COMMANDS.sh shell script.

    The 5-step equilibration schedule heats in equal increments from
    near 0 K to the target temperature (NVT), then finalises with an
    NPT density-equilibration step.

    Examples
    --------
    >>> writer = OpenMMWriter(replica)
    >>> writer.write_replica_input()
    >>> writer.write_commands()
    """

    #: Number of equilibration stages (4 NVT heating + 1 NPT density)
    N_EQ_STEPS: int = 5
    #: Steps per NVT heating stage (50 ps at 2 fs)
    HEATING_STEPS: int = 25_000
    #: Steps for the NPT density-equilibration stage (100 ps at 2 fs)
    NPT_EQ_STEPS: int = 50_000

    def __init__(self, replica) -> None:
        """
        Initialise writer for *replica*.

        Parameters
        ----------
        replica : Replica
            Replica whose settings drive the generated scripts.
        """
        self.replica = replica
        settings = replica.settings
        if settings is None:
            raise OpenMMWriterError("Replica settings not configured")

        self.engine = OpenMMEngine(
            config=OpenMMConfig(
                timestep=settings.timestep,
                temperature=settings.temperature,
                pressure=1.0,
                restraint_weight=settings.restraint_force if settings.has_restraints else 0.0,
            )
        )
        self.log = logging.getLogger(self.__class__.__name__)

    # ------------------------------------------------------------------
    # Internal helpers
    # ------------------------------------------------------------------

    def _get_restraint_mask(self) -> str | None:
        """Return mask keyword for restraints, or None when FREE."""
        settings = self.replica.settings
        if not settings or settings.restraint_mode.upper() == "FREE":
            return None
        if settings.restraint_mask:
            return settings.restraint_mask
        mode = settings.restraint_mode.upper()
        if mode == "BB":
            return "backbone"
        if mode == "HA":
            return "heavy"
        return "backbone"

    # ------------------------------------------------------------------
    # Public interface
    # ------------------------------------------------------------------

    def write_replica_input(self) -> None:
        """
        Write all OpenMM simulation scripts into the replica directory.

        Creates:

        * ``min/min.py``  — energy minimisation
        * ``eq/eq1.py`` … ``eq/eq5.py``  — equilibration stages
        * ``md/md_{step:03d}.py`` per production step
        """
        if not self.replica.path:
            raise OpenMMWriterError("Replica path not set")
        settings = self.replica.settings
        if not settings:
            raise OpenMMWriterError("Replica settings not configured")

        restr_mask = self._get_restraint_mask()
        top = self.replica.topology_path
        crd = self.replica.coordinates_path

        # --- Minimisation ---
        min_path = self.replica.min_path
        if min_path:
            min_path.mkdir(exist_ok=True)
            self.engine.write_minimization(
                topology=top,
                coordinates=crd,
                output=min_path / "min.py",
                restraint_mask=restr_mask,
            )

        # --- Equilibration ---
        eq_path = self.replica.eq_path
        if eq_path:
            eq_path.mkdir(exist_ok=True)

            # Step 1: NVT, heat from near 0 K to temperature / N_EQ_STEPS
            self.engine.write_equilibration(
                topology=top,
                coordinates=Path("..") / self.replica.min_folder / "min.rst7",
                output=eq_path / "eq1.py",
                steps=self.HEATING_STEPS,
                ensemble="NVT",
                restraint_mask=restr_mask,
            )
            # Steps 2–4: NVT, continue heating
            for i in range(2, 5):
                self.engine.write_equilibration(
                    topology=top,
                    coordinates=Path(f"eq{i - 1}.rst7"),
                    output=eq_path / f"eq{i}.py",
                    steps=self.HEATING_STEPS,
                    ensemble="NVT",
                    restraint_mask=restr_mask,
                )
            # Step 5: NPT density equilibration at target temperature
            self.engine.write_equilibration(
                topology=top,
                coordinates=Path("eq4.rst7"),
                output=eq_path / "eq5.py",
                steps=self.NPT_EQ_STEPS,
                ensemble="NPT",
                restraint_mask=restr_mask,
            )

        # --- Production (one script per step) ---
        md_path = self.replica.md_path
        if md_path:
            md_path.mkdir(exist_ok=True)
            for step in range(1, settings.n_traj_files + 1):
                if step == 1:
                    prev_rst = Path("..") / self.replica.eq_folder / "eq5.rst7"
                else:
                    prev_name = self.replica.md_output_template.format(
                        step=step - 1, extension="rst7"
                    )
                    prev_rst = Path(prev_name)

                out_prefix = self.replica.md_output_template.format(step=step, extension="").rstrip(
                    "."
                )
                script_name = f"{out_prefix}.py"

                self.engine.write_production(
                    topology=top,
                    coordinates=prev_rst,
                    output=md_path / script_name,
                    steps=settings.prod_steps,
                    ensemble="NVT",
                    trajectory_freq=settings.traj_frequency,
                )

        self.log.info(f"Wrote OpenMM scripts for replica {self.replica.name}")

    def write_commands(self, output: Path | str = "COMMANDS.sh") -> None:
        """
        Write a COMMANDS.sh shell script that runs all simulation stages.

        Parameters
        ----------
        output : Path | str
            Script filename (relative to replica path).
        """
        if not self.replica.path:
            raise OpenMMWriterError("Replica path not set")
        settings = self.replica.settings
        if not settings:
            raise OpenMMWriterError("Replica settings not configured")

        lines = [
            "#!/bin/bash",
            f"# OpenMM commands for replica {self.replica.name}",
            "",
            "# Minimisation",
            f"cd {self.replica.min_folder}",
            "python min.py",
            "cd ..",
            "",
            "# Equilibration",
            f"cd {self.replica.eq_folder}",
        ]
        for i in range(1, self.N_EQ_STEPS + 1):
            lines.append(f"python eq{i}.py")
        lines.extend(["cd ..", ""])

        lines.extend(
            [
                "# Production",
                f"cd {self.replica.md_folder}",
            ]
        )
        for step in range(1, settings.n_traj_files + 1):
            script_prefix = self.replica.md_output_template.format(step=step, extension="").rstrip(
                "."
            )
            lines.append(f"# Step {step}")
            lines.append(f"python {script_prefix}.py")
        lines.extend(["cd ..", ""])

        script_path = self.replica.path / output
        script_path.write_text("\n".join(lines))
        script_path.chmod(0o755)
        self.log.info(f"Wrote commands script: {script_path}")


# =============================================================================
# OpenMMChecker - Verify OpenMM Simulation Completion
# =============================================================================


class OpenMMChecker:
    """
    Check OpenMM simulation completion status.

    Completion is determined by the presence of non-empty output files
    (``.log``, ``.rst7``, or ``.chk``) in the appropriate sub-folders.

    Examples
    --------
    >>> checker = OpenMMChecker(replica)
    >>> if checker.check_production([1, 2, 3]):
    ...     print("First 3 production steps complete")
    """

    #: Must match OpenMMWriter.N_EQ_STEPS
    N_EQ_STEPS: int = 5

    def __init__(self, replica, warn: bool = True) -> None:
        """
        Parameters
        ----------
        replica : Replica
            Replica to inspect.
        warn : bool
            Emit warnings for incomplete stages.
        """
        self.replica = replica
        self.warn = warn
        self.log = logging.getLogger(self.__class__.__name__)

    # ------------------------------------------------------------------
    # Stage checks
    # ------------------------------------------------------------------

    def check_minimization(self) -> bool:
        """Check whether minimisation completed successfully."""
        min_path = self.replica.min_path
        if not min_path or not min_path.exists():
            if self.warn:
                self.log.warning(f"Minimisation folder not found: {min_path}")
            return False

        for fname in ("min.log", "min.rst7"):
            f = min_path / fname
            if f.exists() and f.stat().st_size > 0:
                return True

        if self.warn:
            self.log.warning("Minimisation not complete: min.log / min.rst7 not found or empty")
        return False

    def check_equilibration(self, step_selection: list[int] | None = None) -> bool:
        """
        Check whether equilibration steps completed.

        Parameters
        ----------
        step_selection : list[int] | None
            Steps to verify.  Defaults to all ``N_EQ_STEPS`` steps.
        """
        eq_path = self.replica.eq_path
        if not eq_path or not eq_path.exists():
            if self.warn:
                self.log.warning(f"Equilibration folder not found: {eq_path}")
            return False

        if step_selection is None:
            step_selection = list(range(1, self.N_EQ_STEPS + 1))

        for step in step_selection:
            ok = any(
                (eq_path / f"eq{step}.{ext}").exists()
                and (eq_path / f"eq{step}.{ext}").stat().st_size > 0
                for ext in ("rst7", "log", "chk")
            )
            if not ok:
                if self.warn:
                    self.log.warning(f"Equilibration step {step} not complete")
                return False
        return True

    def check_production(self, step_selection: list[int] | None = None) -> bool:
        """
        Check whether production steps completed.

        Parameters
        ----------
        step_selection : list[int] | None
            Steps to verify.  Defaults to all expected steps.
        """
        md_path = self.replica.md_path
        if not md_path or not md_path.exists():
            if self.warn:
                self.log.warning(f"Production folder not found: {md_path}")
            return False

        if step_selection is None:
            if self.replica.settings:
                step_selection = list(range(1, self.replica.settings.n_traj_files + 1))
            else:
                step_selection = [1]

        extensions = self.replica.check_production_extension(step_selection)
        for step in step_selection:
            if extensions.get(step) is None:
                if self.warn:
                    self.log.warning(f"Production step {step} trajectory not found")
                return False
        return True

    def get_sim_volume(self, step: int = 1) -> float | None:
        """
        Get simulation box volume (Å³) from a production restart file.

        Uses parmed to read the ``.rst7`` file written at the end of each
        production step.  Handles both orthorhombic and triclinic boxes.

        Parameters
        ----------
        step : int
            Production step number.

        Returns
        -------
        float | None
            Box volume in Å³, or None if unavailable.
        """
        md_path = self.replica.md_path
        if not md_path:
            return None

        rst_name = self.replica.md_output_template.format(step=step, extension="rst7")
        rst_file = md_path / rst_name
        if not rst_file.exists():
            return None

        try:
            import math

            import parmed

            rst = parmed.load_file(str(rst_file))
            if rst.box is not None:
                a, b, c = float(rst.box[0]), float(rst.box[1]), float(rst.box[2])
                alpha = float(rst.box[3])
                beta = float(rst.box[4])
                gamma_ang = float(rst.box[5])
                if (
                    abs(alpha - 90.0) < 0.01
                    and abs(beta - 90.0) < 0.01
                    and abs(gamma_ang - 90.0) < 0.01
                ):
                    return a * b * c
                cos_a = math.cos(math.radians(alpha))
                cos_b = math.cos(math.radians(beta))
                cos_g = math.cos(math.radians(gamma_ang))
                vol = (
                    a
                    * b
                    * c
                    * math.sqrt(1.0 - cos_a**2 - cos_b**2 - cos_g**2 + 2.0 * cos_a * cos_b * cos_g)
                )
                return vol
        except Exception as e:
            self.log.warning(f"Could not read box volume from {rst_file}: {e}")
        return None
