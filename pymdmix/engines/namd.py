class NAMDChecker:
    """
    Check NAMD simulation completion status.

    Mirrors :class:`~pymdmix.engines.amber.AmberChecker` in interface.

    Examples
    --------
    >>> checker = NAMDChecker(replica)
    >>> if checker.check_production([1, 2, 3]):
    ...     print("First 3 steps complete")
    """

    def __init__(self, replica, warn: bool = True) -> None:
        self.replica = replica
        self.warn = warn
        self.log = logging.getLogger(self.__class__.__name__)

    def _read_out(self, path: Path) -> str | None:
        """Return file content or None if the file is missing."""
        if path.exists():
            return path.read_text()
        return None

    def check_minimization(self) -> bool:
        """Return ``True`` if minimization completed successfully."""
        min_path = self.replica.min_path
        if not min_path or not min_path.exists():
            if self.warn:
                self.log.warning(f"Minimization folder not found: {min_path}")
            return False

        out = self._read_out(min_path / "min.out")
        if out is None:
            if self.warn:
                self.log.warning("Minimization output file not found")
            return False

        if _NAMD_COMPLETE in out:
            return True

        if self.warn:
            self.log.warning("Minimization not complete (no NAMD completion marker)")
        return False

    def check_equilibration(
        self,
        step_selection: list[int] | None = None,
    ) -> bool:
        """
        Return ``True`` if all selected equilibration steps completed.

        Parameters
        ----------
        step_selection : list[int] | None
            Steps to check (1-based). Defaults to [1, 2].
        """
        eq_path = self.replica.eq_path
        if not eq_path or not eq_path.exists():
            if self.warn:
                self.log.warning(f"Equilibration folder not found: {eq_path}")
            return False

        steps = step_selection if step_selection is not None else [1, 2]

        for step in steps:
            out = self._read_out(eq_path / f"eq{step}.out")
            if out is None:
                if self.warn:
                    self.log.warning(f"Equilibration step {step} output not found")
                return False
            if _NAMD_COMPLETE not in out:
                if self.warn:
                    self.log.warning(f"Equilibration step {step} not complete")
                return False

        return True

    def check_production(
        self,
        step_selection: list[int] | None = None,
    ) -> bool:
        """
        Return ``True`` if all selected production steps completed.

        Parameters
        ----------
        step_selection : list[int] | None
            Steps to check (1-based). Defaults to all expected steps.
        """
        md_path = self.replica.md_path
        if not md_path or not md_path.exists():
            if self.warn:
                self.log.warning(f"Production folder not found: {md_path}")
            return False

        if step_selection is None:
            settings = self.replica.settings
            step_selection = list(range(1, settings.n_traj_files + 1)) if settings else [1]

        for step in step_selection:
            step_name = self.replica.md_output_template.format(step=step, extension="").rstrip(".")
            out = self._read_out(md_path / f"{step_name}.out")
            if out is None:
                if self.warn:
                    self.log.warning(f"Production step {step} output not found")
                return False
            if _NAMD_COMPLETE not in out:
                if self.warn:
                    self.log.warning(f"Production step {step} not complete")
                return False

        return True

    def get_sim_volume(self, step: int = 1) -> float | None:
        """
        Parse box volume from the NAMD extended-system file (.xsc).

        The .xsc format stores the three box vectors; for an orthorhombic
        box only the diagonal elements are non-zero.

        Parameters
        ----------
        step : int
            Production step whose .xsc file to read.

        Returns
        -------
        float | None
            Box volume in Å³, or ``None`` if the file cannot be parsed.
        """
        import numpy as np

        md_path = self.replica.md_path
        if not md_path:
            return None

        step_name = self.replica.md_output_template.format(step=step, extension="").rstrip(".")
        xsc_path = md_path / f"{step_name}.xsc"

        if not xsc_path.exists():
            self.log.warning(f"XSC file not found: {xsc_path}")
            return None

        try:
            # XSC format: first two lines are comments; third line contains
            # step  a1x a1y a1z  a2x a2y a2z  a3x a3y a3z  ox oy oz  ...
            lines = xsc_path.read_text().splitlines()
            data_lines = [line for line in lines if not line.startswith("#") and line.strip()]
            if not data_lines:
                return None

            values = list(map(float, data_lines[-1].split()))
            # Columns 1-3: first vector, 4-6: second, 7-9: third
            vec_a = np.array(values[1:4])
            vec_b = np.array(values[4:7])
            vec_c = np.array(values[7:10])

            # Volume = |a · (b × c)|
            vol = abs(np.dot(vec_a, np.cross(vec_b, vec_c)))
            return float(vol)

        except Exception as exc:
            self.log.warning(f"Could not parse XSC file {xsc_path}: {exc}")
            return None
