# NAMDWriter - Replica-level input file generation
# =============================================================================

# Completion string that NAMD writes at the end of a successful run
_NAMD_COMPLETE = "End of program"


class NAMDWriterError(Exception):
    """Error raised by NAMDWriter."""


class NAMDWriter:
    """
    Write NAMD input files for a :class:`~pymdmix.project.replica.Replica`.

    Mirrors :class:`~pymdmix.engines.amber.AmberWriter` in interface.

    Examples
    --------
    >>> from pymdmix.engines.namd import NAMDWriter
    >>> writer = NAMDWriter(replica)
    >>> writer.write_replica_input()
    >>> writer.write_commands()
    """

    def __init__(self, replica) -> None:
        self.replica = replica
        self.engine = NAMDEngine(
            NAMDConfig(
                timestep=replica.settings.timestep if replica.settings else 2.0,
                temperature=replica.settings.temperature if replica.settings else 300.0,
            )
        )
        self.log = logging.getLogger(self.__class__.__name__)

    # ------------------------------------------------------------------
    # Helpers
    # ------------------------------------------------------------------

    def _get_restraint_mask(self) -> str:
        """Return the Amber restraint mask string for this replica."""
        settings = self.replica.settings
        if not settings:
            return ""
        if settings.restraint_mask:
            return settings.restraint_mask
        mode = settings.restraint_mode.upper()
        if mode == "FREE":
            return ""
        if mode == "BB":
            return "@CA,C,N,O"
        if mode == "HA":
            return "!@H="
        return "@CA"

    def _write_restraint_pdb(self) -> Path | None:
        """
        Create *restraints.pdb* in the replica folder.

        Sets the B-factor column to ``restraint_force`` for atoms
        matching the restraint mask, and 0 for all others.

        Returns the path if written, ``None`` if no restraints are needed.
        """
        import parmed

        settings = self.replica.settings
        if not settings or settings.restraint_mode.upper() == "FREE":
            return None

        pdb_path = self.replica.pdb_path
        if pdb_path is None or not pdb_path.exists():
            self.log.warning("pdb_path not set/found; cannot write restraints.pdb")
            return None

        out_path = self.replica.path / "restraints.pdb"

        try:
            struct = parmed.load_file(str(pdb_path))
            force = settings.restraint_force

            # Identify restrained atoms
            mask_str = self._get_restraint_mask()
            if mask_str:
                amber_mask = parmed.amber.AmberMask(struct, mask_str)
                restrained = set(amber_mask.Selected())
            else:
                restrained = set()

            for i, atom in enumerate(struct.atoms):
                atom.bfactor = force if i in restrained else 0.0

            struct.save(str(out_path), overwrite=True)
            self.log.debug(f"Wrote restraints.pdb → {out_path}")
            return out_path

        except Exception as exc:
            self.log.warning(f"Could not write restraints.pdb: {exc}")
            return None

    # ------------------------------------------------------------------
    # Public interface
    # ------------------------------------------------------------------

    def write_replica_input(self) -> None:
        """Write min, eq1, eq2, and per-step production .namd files."""
        replica = self.replica
        if not replica.path:
            raise NAMDWriterError("Replica path not set")

        settings = replica.settings
        if not settings:
            raise NAMDWriterError("Replica settings not configured")

        has_restraints = settings.has_restraints
        restraint_pdb: str | None = None

        if has_restraints:
            rp = self._write_restraint_pdb()
            restraint_pdb = str(rp) if rp else None

        top = replica.topology_path
        crd = replica.coordinates_path
        if top is None or crd is None:
            raise NAMDWriterError("Replica topology/coordinates not set")

        # ---- Minimization ------------------------------------------------
        min_path = replica.min_path
        if min_path:
            min_path.mkdir(exist_ok=True)
            self.engine.write_minimization(
                topology=top,
                coordinates=crd,
                output=min_path / "min.namd",
                steps=5000,
                restraint_pdb=restraint_pdb,
            )

        # ---- Equilibration -----------------------------------------------
        eq_path = replica.eq_path
        if eq_path:
            eq_path.mkdir(exist_ok=True)

            # eq1: NVT heating from 0 K → target temperature
            self.engine.write_heating(
                topology=top,
                coordinates=crd,
                output=eq_path / "eq1.namd",
                steps=50000,
                start_temp=0.0,
                target_temp=settings.temperature,
                restraint_pdb=restraint_pdb,
            )

            # eq2: NPT equilibration at target temperature
            self.engine.write_equilibration(
                topology=top,
                coordinates=crd,
                output=eq_path / "eq2.namd",
                steps=100000,
                ensemble="NPT",
                restraint_pdb=restraint_pdb,
            )

        # ---- Production --------------------------------------------------
        md_path = replica.md_path
        if md_path:
            md_path.mkdir(exist_ok=True)
            for step in range(1, settings.n_traj_files + 1):
                step_name = replica.md_output_template.format(step=step, extension="").rstrip(".")
                self.engine.write_production(
                    topology=top,
                    coordinates=crd,
                    output=md_path / f"{step_name}.namd",
                    steps=settings.prod_steps,
                    ensemble="NVT",
                    dcd_freq=settings.traj_frequency,
                )

        self.log.info(f"Wrote NAMD input files for replica {replica.name}")

    def write_commands(self, output: Path | str = "COMMANDS.sh") -> None:
        """Write a shell script that runs all stages sequentially."""
        replica = self.replica
        if not replica.path:
            raise NAMDWriterError("Replica path not set")

        settings = replica.settings
        if not settings:
            raise NAMDWriterError("Replica settings not configured")

        namd_exe = "namd2"

        lines = [
            "#!/bin/bash",
            f"# Commands for replica {replica.name} (NAMD)",
            "",
            f"NAMD={namd_exe}",
            "",
            "# Minimization",
            f"cd {replica.min_folder}",
            "$NAMD min.namd > min.out",
            "cd ..",
            "",
            "# Equilibration",
            f"cd {replica.eq_folder}",
            "$NAMD eq1.namd > eq1.out",
            "$NAMD eq2.namd > eq2.out",
            "cd ..",
            "",
            "# Production",
            f"cd {replica.md_folder}",
        ]

        for step in range(1, settings.n_traj_files + 1):
            step_name = replica.md_output_template.format(step=step, extension="").rstrip(".")
            lines.append(f"$NAMD {step_name}.namd > {step_name}.out")

        lines.extend(["cd ..", ""])

        script = "\n".join(lines)
        script_path = replica.path / output
        script_path.write_text(script)
        script_path.chmod(0o755)

        self.log.info(f"Wrote NAMD commands script: {script_path}")


